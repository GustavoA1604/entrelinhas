// Generates src/data/trivia-stats.js from the game's word lists.
// Run: node scripts/gen-trivia-stats.js
//
// Facts are split into three pools the picker draws from:
//   highlights: a small, hand-tuned set of genuinely interesting stats
//   longtail:   one fact per letter (occurrences + starts-with), the obscure
//               long tail; the picker hits it only rarely
//   game:       numeric "how it works" facts (corpus sizes) that would drift
//               if hard-coded, so they live with the generated data
import { writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { VALID } from "../src/data/valid.js";
import { ANSWERS } from "../src/data/answers.js";

const LETTERS = "abcdefghijklmnopqrstuvwxyz".split("");
const VOWELS = new Set(["a", "e", "i", "o", "u"]);

// Brazilian number formatting: 5582 → "5.582", 14.23 → "14,2".
const fmt = (n) => n.toLocaleString("pt-BR");
const pct = (n, total) => ((n / total) * 100).toLocaleString("pt-BR", { maximumFractionDigits: 1 });
// "uma única vez" / "37 vezes". Keep singular/plural truthful.
const vezes = (n) => (n === 1 ? "uma única vez" : `${fmt(n)} vezes`);

const words = [...VALID];
const N = words.length;

const occ = Object.fromEntries(LETTERS.map((l) => [l, 0])); // total appearances
const starts = Object.fromEntries(LETTERS.map((l) => [l, 0])); // words starting with
const ends = Object.fromEntries(LETTERS.map((l) => [l, 0])); // words ending with
let doubleWord = 0; // words with a doubled letter (carro, massa)
let noRepeatWords = 0; // words with 5 distinct letters
let vowelTotal = 0;

for (const w of words) {
  starts[w[0]]++;
  ends[w[4]]++;
  const distinct = new Set();
  let hasDouble = false;
  for (let i = 0; i < 5; i++) {
    const ch = w[i];
    occ[ch]++;
    distinct.add(ch);
    if (VOWELS.has(ch)) vowelTotal++;
    if (i < 4 && w[i] === w[i + 1]) hasDouble = true;
  }
  if (hasDouble) {
    doubleWord++;
  }
  if (distinct.size === 5) noRepeatWords++;
}

const byOcc = [...LETTERS].sort((a, b) => occ[b] - occ[a]);
const present = byOcc.filter((l) => occ[l] > 0);
const absent = LETTERS.filter((l) => occ[l] === 0);
const topStart = [...LETTERS].sort((a, b) => starts[b] - starts[a])[0];
const topEnd = [...LETTERS].sort((a, b) => ends[b] - ends[a])[0];
const mostCommon = present[0];
const rarest = present[present.length - 1];
const up = (l) => l.toUpperCase();

const highlights = [
  `A letra mais comum em Entrelinhas é o '${up(mostCommon)}': aparece ${vezes(occ[mostCommon])}.`,
  `A letra menos comum que ainda aparece em Entrelinhas é o '${up(rarest)}', com ${vezes(occ[rarest])}.`,
  `Mais palavras começam com '${up(topStart)}' do que com qualquer outra letra.`,
  `${pct(starts[topStart], N)}% das palavras cadastradas em Entrelinhas começam com '${up(topStart)}'.`,
  `${pct(ends[topEnd], N)}% das palavras cadastradas em Entrelinhas terminam em '${up(topEnd)}'.`,
  `${pct(doubleWord, N)}% das palavras cadastradas em Entrelinhas têm uma letra dobrada, como em 'saara'.`,
  `${pct(noRepeatWords, N)}% das palavras cadastradas em Entrelinhas não repetem nenhuma letra.`,
  `Em média, cada palavra de Entrelinhas tem ${(vowelTotal / N).toLocaleString("pt-BR", { maximumFractionDigits: 1 })} vogais.`,
];
if (absent.length === 1) {
  highlights.push(`A letra '${up(absent[0])}' não aparece em nenhuma palavra de Entrelinhas.`);
} else if (absent.length > 1) {
  const list = absent.map(up).join(", ");
  highlights.push(`As letras ${list} não aparecem em nenhuma palavra de Entrelinhas.`);
}

const longtail = [];
for (const l of LETTERS) {
  longtail.push(
    occ[l] === 0
      ? `A letra '${up(l)}' não aparece em nenhuma palavra de Entrelinhas.`
      : `A letra '${up(l)}' aparece ${vezes(occ[l])} em Entrelinhas.`,
  );
}
for (const l of LETTERS) {
  longtail.push(
    starts[l] === 0
      ? `Nenhuma palavra de Entrelinhas começa com '${up(l)}'.`
      : starts[l] === 1
        ? `Apenas uma palavra de Entrelinhas começa com '${up(l)}'.`
        : `${fmt(starts[l])} palavras de Entrelinhas começam com '${up(l)}'.`,
  );
}

const game = [
  `Entrelinhas aceita ${fmt(N)} palavras de 5 letras como palpite.`,
  `Existem ${fmt(ANSWERS.length)} palavras que podem ser a resposta do dia em Entrelinhas.`,
];

const lines = (arr) => arr.map((s) => `  ${JSON.stringify(s)},`).join("\n");
const out = `// Auto-generated by scripts/gen-trivia-stats.js. Do not edit by hand.
// Trivia facts derived from the accepted-guess dictionary (VALID).
export const STATS_HIGHLIGHTS = [
${lines(highlights)}
];

export const STATS_LONGTAIL = [
${lines(longtail)}
];

export const STATS_GAME = [
${lines(game)}
];
`;

const target = fileURLToPath(new URL("../src/data/trivia-stats.js", import.meta.url));
await writeFile(target, out);
console.log(`Wrote ${target}`);
console.log(
  `  highlights: ${highlights.length}, longtail: ${longtail.length}, game: ${game.length}`,
);
